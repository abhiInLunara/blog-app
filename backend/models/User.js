const mongoose = require("mongoose");

// Defining user schema
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//  Exporting the user model

module.exports = mongoose.model("User", userSchema);
